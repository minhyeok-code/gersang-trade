package org.example.gersangtrade.domain.listing.enums;

/**
 * 거래 묶음(Bundle) 유형.
 * MATERIAL_BUNDLE: 재료 묶음 거래,
 * EQUIPMENT_SINGLE: 장비 단품 거래,
 * EQUIPMENT_SET: 장비 세트 묶음 거래
 */
public enum BundleType {
    MATERIAL_BUNDLE,   // 재료 묶음
    EQUIPMENT_SINGLE,  // 장비 단품
    EQUIPMENT_SET      // 장비 세트 풀셋
}
